    private void jButtonEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditarActionPerformed
         
        
         String bairro =   jTextFieldBairro.getText().toUpperCase();
         String rua =  jTextFieldRua.getText().toUpperCase();
         String cidade =  jTextFieldCidade.getText().toUpperCase();
         String numero =   jTextFieldNumero.getText().toUpperCase();
         String telefone = jTextFieldTelefone.getText().toUpperCase();
         String cep = jTextFieldCEP.getText().toUpperCase();
         String complemento = jTextFieldComplemento.getText().toUpperCase();
         String nome = jTextFieldNome.getText().toUpperCase();
        
           
         if(!bairro.equals("") && !rua.equals("") && !cidade.equals("") && !numero.equals("") && !telefone.equals("") && !cep.equals("") && !complemento.equals("") && !nome.equals("")){
         SQLiteJDBCDriverConnection.update(conn, bairro, telefone, rua, cep, complemento, cidade, nome);
         JOptionPane.showMessageDialog(null, "Dados atualizados com sucesso!");
         limparCampos();         
         }else{
          JOptionPane.showMessageDialog(null, "Existe um ou mais campos vazios!");
         }
        

    }//GEN-LAST:event_jButtonEditarActionPerformed

